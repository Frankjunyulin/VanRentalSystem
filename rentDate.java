import java.util.Calendar;

public class rentDate {
	Calendar begin;
	Calendar end;
	int id;
	
	
	/**
	 * The constructor of rentDate
	 * @param begin
	 * @param end
	 * @param id
	 */
	public rentDate(Calendar begin, Calendar end, int id) {
		super();
		this.begin = begin;
		this.end = end;
		this.id = id;
	}







}
